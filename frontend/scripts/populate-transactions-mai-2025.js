// Utility functions
function randomAmount(min, max) {
  const amount = Math.random() * (max - min) + min;
  // 70% »ôanse pentru numere √Æntregi, 30% pentru decimale
  if (Math.random() > 0.3) {
    return Math.round(amount);
  }
  return Math.round(amount * 100) / 100;
}

function randomDate(year, month) {
  const daysInMonth = new Date(year, month, 0).getDate();
  const randomDay = Math.floor(Math.random() * daysInMonth) + 1;
  return `${year}-${month.toString().padStart(2, '0')}-${randomDay.toString().padStart(2, '0')}`;
}

function randomDescription(descriptions) {
  return descriptions[Math.floor(Math.random() * descriptions.length)];
}

// Generare tranzac»õii realiste
function generateRealisticTransactions() {
  const transactions = [];
  
  console.log('üéØ Generez tranzac»õii realiste pentru Mai 2025...\n');
  
  TRANSACTION_TEMPLATES.forEach((template, index) => {
    // Verific dacƒÉ template-ul ar trebui sƒÉ aparƒÉ (probabilitate)
    if (Math.random() > template.probability) {
      console.log(`‚è≠Ô∏è  SƒÉrit: ${template.subcategory} (probabilitate ${template.probability})`);
      return;
    }
    
    // Determin c√¢te tranzac»õii sƒÉ generez pentru acest template
    const count = Math.min(template.frequency, Math.ceil(template.frequency * Math.random()));
    
    console.log(`üìÇ ${template.category} > ${template.subcategory}: ${count} tranzac»õii`);
    
    for (let i = 0; i < count; i++) {
      const amount = randomAmount(template.amount[0], template.amount[1]);
      const transaction = {
        user_id: CONFIG.USER_ID,
        type: template.type,
        amount: amount.toString(),
        category: template.category,
        subcategory: template.subcategory,
        description: randomDescription(template.descriptions),
        date: randomDate(CONFIG.YEAR, CONFIG.MONTH),
        currency: CONFIG.CURRENCY,
        recurring: false,
        frequency: null
      };
      
      transactions.push(transaction);
      console.log(`  ‚úÖ ${amount} lei - ${transaction.description}`);
    }
    console.log('');
  });
  
  // Sortez dupƒÉ datƒÉ
  transactions.sort((a, b) => new Date(a.date) - new Date(b.date));
  
  console.log(`üìä Total tranzac»õii generate: ${transactions.length}`);
  return transactions;
}

// Inserare √Æn Supabase
async function insertTransactions(transactions) {
  console.log('\nüíæ √éncep inserarea √Æn Supabase...\n');
  
  let successCount = 0;
  let errorCount = 0;
  
  // Inserez √Æn batch-uri de c√¢te 10 pentru performan»õƒÉ
  const batchSize = 10;
  for (let i = 0; i < transactions.length; i += batchSize) {
    const batch = transactions.slice(i, i + batchSize);
    
    console.log(`üì¶ Batch ${Math.floor(i/batchSize) + 1}/${Math.ceil(transactions.length/batchSize)}: ${batch.length} tranzac»õii`);
    
    try {
      const { data, error } = await supabase
        .from('transactions')
        .insert(batch);
      
      if (error) {
        console.error(`‚ùå Eroare batch: ${error.message}`);
        errorCount += batch.length;
      } else {
        console.log(`‚úÖ Batch insertat cu succes!`);
        successCount += batch.length;
      }
    } catch (err) {
      console.error(`üí• Eroare criticƒÉ batch: ${err.message}`);
      errorCount += batch.length;
    }
    
    // PauzƒÉ scurtƒÉ √Æntre batch-uri
    await new Promise(resolve => setTimeout(resolve, 200));
  }
  
  return { successCount, errorCount };
}

